const path = require(`path`)
const { createFilePath } = require(`gatsby-source-filesystem`)

exports.createPages = ({ graphql, actions }) => {
  const { createPage } = actions

  const portfolio = path.resolve(`./src/templates/portfolio.js`)
  return graphql(
    `
      {
        allMarkdownRemark(
          sort: { fields: [frontmatter___date], order: DESC }
          limit: 1000
        ) {
          edges {
            node {
              fields {
                slug
              }
              frontmatter {
                path
                title
              }
            }
          }
        }
      }
    `
  ).then(result => {
    if (result.errors) {
      throw result.errors
    }

    // Create portfolio pages.
    const portfolioPages = result.data.allMarkdownRemark.edges

    portfolioPages.forEach((post, index) => {
      const previous =
        index === portfolioPages.length - 1
          ? null
          : portfolioPages[index + 1].node
      const next = index === 0 ? null : portfolioPages[index - 1].node

      createPage({
        path: post.node.frontmatter.path,
        component: portfolio,
        context: {
          slug: post.node.fields.slug,
          previous,
          next,
        },
      })
    })

    return null
  })
}

exports.onCreateNode = ({ node, actions, getNode }) => {
  const { createNodeField } = actions

  if (node.internal.type === `MarkdownRemark`) {
    const value = createFilePath({ node, getNode })
    createNodeField({
      name: `slug`,
      node,
      value,
    })
  }
}
